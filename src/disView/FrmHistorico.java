    private void pesquisar8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesquisar8ActionPerformed
        // BotÃ£o buscar Venda Por Periodo //

        //receber as Datas//
        try {
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate data_inicio = LocalDate.parse(txtdatainicio.getText(), formato);
            LocalDate data_fim = LocalDate.parse(txtdatafim.getText(), formato);
            double totalPeriodo;

            VendasDAO dao = new VendasDAO();
            List<Vendas> lista = dao.listarVendasPorPeriodo(data_inicio, data_fim);
            totalPeriodo = dao.totalPorPeriodo(data_inicio, data_fim);
            txttotal_periodo.setText(String.valueOf(totalPeriodo));
            DefaultTableModel dados = (DefaultTableModel) tabelahistorico.getModel();
            dados.setNumRows(0);

            for (Vendas v : lista) {
                dados.addRow(new Object[]{
                    v.getId(),
                    v.getData_venda(),
                    v.getCliente().getNome(),
                    v.getTotal_venda(),
                    v.getObs()

                });

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "DATA NÃO ENCONTRADA OU DIGITE UMA DATA VALIDA");
        }

    }//GEN-LAST:event_pesquisar8ActionPerformed

    private void tabelahistoricoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelahistoricoMouseClicked
        //Clicar em uma Venda //
        FrmDetalheVenda tela = new FrmDetalheVenda();

        tela.txtnomecliente3.setText(tabelahistorico.getValueAt(tabelahistorico.getSelectedRow(), 2).toString());
        tela.txttotavenda.setText(tabelahistorico.getValueAt(tabelahistorico.getSelectedRow(), 3).toString());
        tela.txtdata.setText(tabelahistorico.getValueAt(tabelahistorico.getSelectedRow(), 1).toString());
        tela.txtobs.setText(tabelahistorico.getValueAt(tabelahistorico.getSelectedRow(), 4).toString());

        int venda_id = Integer.parseInt(tabelahistorico.getValueAt(tabelahistorico.getSelectedRow(), 0).toString());

        // dados dos itens comprados //
        ItemVendas item = new ItemVendas();
        ItemVendaDAO dao_item = new ItemVendaDAO();

        List<ItemVendas> listaitens = dao_item.listarItensPorVendao(venda_id);

        DefaultTableModel dados = (DefaultTableModel) tela.tabeladetalhesvenda.getModel();
        dados.setNumRows(0);

        for (ItemVendas c : listaitens) {
            dados.addRow(new Object[]{
                c.getProduto().getDescricao(),
                c.getQtd(),
                c.getProduto().getPreco_de_venda(),
                c.getSubtotal()

            });

        }

        tela.setVisible(true);


    }//GEN-LAST:event_tabelahistoricoMouseClicked

